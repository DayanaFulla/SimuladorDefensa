package Modulos;

/**
 *
 * @author dayan
 */
public class Area {
    private int id;
    private String titulo;

    public Area() {
    }

    public Area(int id, String titulo) {
        this.id = id;
        this.titulo = titulo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }
    
    
    
}
